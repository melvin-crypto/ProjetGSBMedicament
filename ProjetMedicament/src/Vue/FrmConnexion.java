    private void txtMailFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtMailFocusGained
           if(txtMail.getText().compareTo("Adresse Mail")==0)
        {
            txtMail.setText("");
            txtMail.setForeground(Color.black);   
            txtMail.setBorder(BorderFactory.createMatteBorder(2,2,2,2, Color.black));
            txtMail.setBackground(Color.white);
          }
    }//GEN-LAST:event_txtMailFocusGained

    private void txtMailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtMailFocusLost
          txtMail.setBorder(BorderFactory.createTitledBorder(""));
        if(txtMail.getText().compareTo("")==0)
        {
            txtMail.setText("Adresse Mail");
            txtMail.setForeground(new Color(204,204, 204));
        } 
    }//GEN-LAST:event_txtMailFocusLost

