    private void btnValiderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnValiderMouseClicked
     
        
         if(txtMail.getText().compareTo("") == 0 || txtMail.getText().compareTo("Adresse Mail") == 0 )
        {
            lblerreur.setText("Saisir un login");
        }
        else if(txtMdp.getText().compareTo("") == 0 || txtMail.getText().compareTo("Mot de Passe") == 0)
        {
            lblerreur.setText("Saisir un mot de passe");
        }
        else
        {
            Utilisateur unUser = fm.GetUnUser(txtMail.getText(), txtMdp.getText());
            if(unUser == null)
            {
                lblerreur.setText("Identifiants incorrects");
                
            }
            else
            {
            //Si utilisateur existe
              FrmSommaire frm = new FrmSommaire();
              frm.setVisible(true);
              this.setVisible(false);
            }
        }
        
    }//GEN-LAST:event_btnValiderMouseClicked

